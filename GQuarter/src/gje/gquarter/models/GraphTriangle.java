public class GraphTriangle {
	public GraphVertex vertexA;
	public GraphVertex vertexB;
	public GraphVertex vertexC;
	
	public GraphTriangle(GraphVertex vertexA, GraphVertex vertexB, GraphVertex vertexC) {
		this.vertexA = vertexA;
		this.vertexB = vertexB;
		this.vertexC = vertexC;
	}
	
}
